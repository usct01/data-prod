import pandas as pd

def parse_timestamp(timestamp_str):
    formats = [
        '%Y_%b_%d_%H:%M:%S',
        '%Y_%m_%d_%H:%M:%S',
        '%Y-%m-%d %H:%M:%S',
        '%Y-%b-%d %H:%M:%S','%Y-%m-%d'
    ]
    for fmt in formats:
        try:
            return pd.to_datetime(timestamp_str, format=fmt)
        except ValueError:
            pass
    raise ValueError(f"Failed to parse timestamp: {timestamp_str}")

